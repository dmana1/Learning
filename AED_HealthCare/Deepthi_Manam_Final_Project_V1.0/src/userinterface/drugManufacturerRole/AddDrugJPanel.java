    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        // TODO add your handling code here:
        String drugName = drugNameJTextField.getText();
        String ingredients = ingredientsJTextField.getText();
        String expiryDate = expiryDateJTextField.getText();
        String treatmentArea = treatmentAreaJTextField.getText();
        String warning = warningsJTextArea.getText();
        String usageDirections = usageDirectionsJTextArea.getText();
        
        if(drugName.isEmpty()|| ingredients.isEmpty() || expiryDate.isEmpty() || treatmentArea.isEmpty() || warning.isEmpty() || usageDirections.isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter values");
        }
        SimpleDateFormat dateFormat = new SimpleDateFormat("MM/dd/yyyy"); 
        Date expirydate = null;
        try {
            expirydate = dateFormat.parse(expiryDate);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Enter Date  value in  MM/dd/yyyy format");
            return;
        }
        
        Drug drug = ((DrugCompanyEnterprise)enterprise).getDrugCatalogue().AddDrug();
        drug.setDrugName(drugName);
        drug.setDrugCompany(enterprise);
        drug.setIngredients(ingredients);
        drug.setExpiryDate(expirydate);
        drug.setTreatmentArea(treatmentArea);
        drug.setWarnings(warning);
        drug.setUsageDirections(usageDirections);
        drug.setIsRemApproved(true);
        JOptionPane.showMessageDialog(null, "Successfully added the drug");

    }//GEN-LAST:event_submitJButtonActionPerformed

