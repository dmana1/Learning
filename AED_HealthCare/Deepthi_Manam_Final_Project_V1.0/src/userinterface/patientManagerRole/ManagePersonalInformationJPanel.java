    private void saveJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveJButtonActionPerformed
        // TODO add your handling code here:
        
        String address = addressJTextField.getText();
        String phoneNumber = phoneNumberJTextField.getText();
        String emailId = emailIdJTextField.getText();
        String firstName = firstNameJTextField.getText();
        String lastName = lastNameJTextField.getText();
        String dobString = dateOfBirthJTextField.getText();
        String ssn = ssnJTextField.getText();
        
        
        if(address.isEmpty() || phoneNumber.isEmpty() || emailId.isEmpty() ||
                firstName.isEmpty() || lastName.isEmpty() || ssn.isEmpty() || dobString.isEmpty()){
            JOptionPane.showMessageDialog(null, "Enter values");
            return;
        }
        if(!ValidationUtil.isValidPhoneNumber(phoneNumber)){
            JOptionPane.showMessageDialog(null, "Enter Valid Phone Number");
            return;
        }
        if(!ValidationUtil.isValidEmailId(emailId)){
            JOptionPane.showMessageDialog(null, "Enter Valid EmailId");
            return;
        }
        if(!ValidationUtil.isValidDate(dobString)){
           JOptionPane.showMessageDialog(null, "Enter date of birth in the MM/DD/YYYY format.");
            return; 
        }
        if(!ValidationUtil.isCompleteSSN(ssn)){
              JOptionPane.showMessageDialog(null, "Enter 9 digits of ssn.");
            return; 
        }
        
        Person person = userAccount.getPerson();
        person.setAddress(address);
        person.setPhoneNumber(Long.parseLong(phoneNumber));
        person.setEmailId(emailId);
        person.setFirstName(firstName);
        person.setLastName(lastName);
        person.setSsn(ssn);
        person.setDateOfBirth(ValidationUtil.parseDate(dobString));
       JOptionPane.showMessageDialog(null, "Successfully updated personal informaion");
        
    }//GEN-LAST:event_saveJButtonActionPerformed


