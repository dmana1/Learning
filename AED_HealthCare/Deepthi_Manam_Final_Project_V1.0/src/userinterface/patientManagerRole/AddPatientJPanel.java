    private void populateAllergyTable(){
        DefaultTableModel dtm = (DefaultTableModel) allergyDetailsJTable.getModel();
        dtm.setRowCount(0);
        allergyDetailsJTable.getTableHeader().setFont(new Font("Tahoma", Font.PLAIN, 18));
        for (Allergy allergy : allergyList) {
            Object[] row = new Object[4];
            row[0] = allergy.getAllergyName();
            row[1] = allergy.getAllergyInformation();
            row[2] = allergy.getStatus();
            dtm.addRow(row);
        }
    }

