    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        // TODO add your handling code here:
        String firstName = firstNameJTextField.getText();
        String lastName = lastNameJTextField.getText();
        String ssn = ssnJTextField.getText();
        String dateOfBirthString = dateOfBirthJTextField.getText();
        String address = addressJTextField.getText();
        String phoneNumberString = phoneNumberJTextField.getText();
      
        String email = emailJTextField.getText();
        boolean isDetailsEnabled = true;
        if (firstName.isEmpty() || lastName.isEmpty() || ssn.isEmpty() || dateOfBirthString.isEmpty() || address.isEmpty()
                || phoneNumberString.isEmpty() || email.isEmpty() ) {
            JOptionPane.showMessageDialog(null, "Enter values");
            return;
        }

         if(!ValidationUtil.isValidPhoneNumber(phoneNumberString)){
             JOptionPane.showMessageDialog(null, "Enter valid phone Number");
            return;
        }
        
        if(!ValidationUtil.isValidEmailId(email)){
            JOptionPane.showMessageDialog(null, "Enter Valid EmailId");
            return;
        }
        if(!ValidationUtil.isValidDate(dateOfBirthString)){
            JOptionPane.showMessageDialog(null, "Enter Valid date in MM/dd/yyyy format");
            return;
        }
        if(!ValidationUtil.isValidSSN(ssn)){
            JOptionPane.showMessageDialog(null, "Enter last 4 digits of ssn");
            return;
        }
        if(yesJRadioButton.isSelected() == false && noJRadioButton.isSelected() == false){
            JOptionPane.showMessageDialog(null, "Please select YES/NO to enable/disable the details for others");
            return;
        }
        //get the uniqueCodeOfpatient
        String uniqueCodeOfPatient = ValidationUtil.generateUniqueCode(firstName, lastName, ssn, ValidationUtil.parseDate(dateOfBirthString));
        
        //Verify if patients profile already exists
        for(Patient patient : ((HospitalEnterprise)enterprise).getPatientDirectory().getPatientsList()){
            String uniqueCodeTemp = ValidationUtil.generateUniqueCode(patient.getFirstName() , patient.getLastName() , patient.getSsn() , patient.getDateOfBirth());
            if(uniqueCodeTemp.equals(uniqueCodeOfPatient)){
                        JOptionPane.showMessageDialog(null, "Patients profile already exists");
                        return;

            }
        }
        
        if(yesJRadioButton.isSelected()){
            isDetailsEnabled = true;
        }
        if(noJRadioButton.isSelected()){
            isDetailsEnabled = false;
        }
       
        //Add patient to the enterprise
        Patient patient = new Patient();
        patient.setFirstName(firstName);
        patient.setLastName(lastName);
        patient.setSsn(ssn);
        patient.setDateOfBirth(ValidationUtil.parseDate(dateOfBirthString));
        patient.setPhoneNumber(ValidationUtil.parsePhoneNumber(phoneNumberString));
        patient.setAddress(address);
        patient.setEmailId(email);
        patient.setAllergyList(allergyList);
        patient.setDetailsEnabled(isDetailsEnabled);
        ((HospitalEnterprise)enterprise).getPatientDirectory().getPatientsList().add(patient);

       
        
        
        JOptionPane.showMessageDialog(null, "Successfully created the patient");

    }//GEN-LAST:event_submitJButtonActionPerformed

