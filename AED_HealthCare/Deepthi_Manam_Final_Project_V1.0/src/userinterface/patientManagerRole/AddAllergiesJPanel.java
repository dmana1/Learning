    private void addAllergyJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addAllergyJButtonActionPerformed
        // TODO add your handling code here:
        //Get the data from fields
        String name = nameJTextField.getText();
        String info = infoJTextField.getText();
        String status = (String)statusJComboBox.getSelectedItem();
        
        //Create an allergy object and add to list
        Allergy a = new Allergy();
        a.setAllergyName(name);
        a.setAllergyInformation(info);
        a.setStatus(status);
        allergyList.add(a);
        JOptionPane.showMessageDialog(null, "Successfully added the allergy");
        clearFields();
    }//GEN-LAST:event_addAllergyJButtonActionPerformed

    private void clearFields(){
        nameJTextField.setText("");
        infoJTextField.setText("");
    }
