    private void searchJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchJButtonActionPerformed
        // TODO add your handling code here:
        String firstName = firstNameJTextField.getText();
        String lastName = lastNameJTextField.getText();
        String ssn = ssnJTextField.getText();
        String dobString = dateOfBirthJTextField.getText();
        
        //validate the front end               
        if(firstName.isEmpty() || lastName.isEmpty() || ssn.isEmpty() || dobString.isEmpty()){
           JOptionPane.showMessageDialog(null, "Enter values");
            return; 
        }
       if(!ValidationUtil.isValidDate(dobString)){
            JOptionPane.showMessageDialog(null, "Enter Valid date in MM/dd/yyyy format");
            return;
        }
       if(!ValidationUtil.isValidSSN(ssn)){
            JOptionPane.showMessageDialog(null, "Enter last 4 digits of SSN");
            return;
       }
        
            
        String uniqueCode1 = ValidationUtil.generateUniqueCode1(firstName, lastName, ssn, dobString);
        boolean isPatientPresent = false;
        for (Patient patient : ((HospitalEnterprise) enterprise).getPatientDirectory().getPatientsList()) {
            String uniqueCode2 = ValidationUtil.generateUniqueCode(patient.getFirstName(), patient.getLastName(), patient.getSsn(), patient.getDateOfBirth());
            if (uniqueCode1.equals(uniqueCode2)) {
                isPatientPresent = true;
                DefaultTableModel model = (DefaultTableModel) appointmentJTable.getModel();
                model.setRowCount(0);
                appointmentJTable.getTableHeader().setFont(new Font("Tahoma", Font.PLAIN, 18));
                Object[] row = new Object[4];
                row[0] = patient.getFirstName();
                row[1] = patient.getLastName();
                row[2] = patient.getSsn();
                row[3] = patient.getDateOfBirth();
                model.addRow(row);
            }

        }
        //if patient profile is not der in db ,show a message to add
        if (!isPatientPresent) {
            JOptionPane.showMessageDialog(null, "Patient does not exist, please add the profile of the patient in hospital");
            return;
        }
    }//GEN-LAST:event_searchJButtonActionPerformed

