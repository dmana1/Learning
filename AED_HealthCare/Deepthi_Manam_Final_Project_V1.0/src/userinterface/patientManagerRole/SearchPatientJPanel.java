    private void appointmentJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_appointmentJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = appointmentJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a patient to book appointment");
            return;
        }
        String firstName = (String) appointmentJTable.getValueAt(0, 0);
        String lastName = (String) appointmentJTable.getValueAt(0, 1);
        String ssn = (String) appointmentJTable.getValueAt(0, 2);
        Date dateOfBirth = (Date) appointmentJTable.getValueAt(0, 3);
        int requestNo =  new Random().nextInt(100);   

        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (organization instanceof DoctorOrganization) {
                AppointmentRequest ar = new AppointmentRequest();
                ar.setUniqueCode(ValidationUtil.generateUniqueCode(firstName, lastName, ssn, dateOfBirth));
                ar.setFirstName(firstName);
                ar.setLastName(lastName);
                ar.setSsn(ssn);
                ar.setDob(dateOfBirth);
                ar.setStatus("Pending");
                ar.setRequestId(requestNo);
                organization.getWorkQueue().getWorkRequestList().add(ar);
            }
        }

        JOptionPane.showMessageDialog(null, "Booked an appointment with doctor");
    }//GEN-LAST:event_appointmentJButtonActionPerformed


