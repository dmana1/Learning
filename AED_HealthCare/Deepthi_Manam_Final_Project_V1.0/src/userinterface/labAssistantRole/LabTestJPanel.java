    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        // TODO add your handling code here:
        String wbcString = wbcJTextField.getText();
        String rbcString = rbcJTextField.getText();
        String ldlString = ldlJTextField.getText();
        String hdlString = hdlJTextField.getText();
        int wbc = 0;
        int rbc = 0;
        int ldl = 0;
        int hdl = 0;

        if (wbcString.isEmpty() || rbcString.isEmpty() || ldlString.isEmpty() || hdlString.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Enter values");
            return;
        }
        try {
            wbc = Integer.parseInt(wbcString);
            rbc = Integer.parseInt(rbcString);
            ldl = Integer.parseInt(ldlString);
            hdl = Integer.parseInt(hdlString);

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Enter valid values");
            return;
        }

        //add the lab test to the patient
        LabTest labTest = new LabTest();
        labTest.setWbc(wbc);
        labTest.setRbc(rbc);
        labTest.setlDL(ldl);
        labTest.sethDL(hdl);

        patient.getLabTestList().add(labTest);
        JOptionPane.showMessageDialog(null, "Successfully submitted the lab results");
       
        
        //set the status to completed
        labWorkRequest.setStatus("Completed");

    }//GEN-LAST:event_submitJButtonActionPerformed

