    private void processJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_processJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = labTestJTable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Select the request");
            return;
        }
       
        
        LabWorkRequest lwr = (LabWorkRequest)labTestJTable.getValueAt(selectedRow, 0);
        if(lwr.getStatus().equals("Completed")){
             JOptionPane.showMessageDialog(null, "Request is already processed");
            return;
        }
        
        //
        LabTestJPanel panel = new LabTestJPanel(userProcessContainer,lwr.getPatient(), lwr);
        userProcessContainer.add("LabTestJPanel", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
        
    }//GEN-LAST:event_processJButtonActionPerformed

