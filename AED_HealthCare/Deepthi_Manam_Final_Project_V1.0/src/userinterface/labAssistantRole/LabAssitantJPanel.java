    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel)labTestJTable.getModel();
        model.setRowCount(0);
        labTestJTable.getTableHeader().setFont(new Font("Tahoma", Font.PLAIN, 18));

        for(WorkRequest wr : organization.getWorkQueue().getWorkRequestList()){
            if(wr instanceof LabWorkRequest){
                Object[] row = new Object[5];
                row[0] = wr;
                row[1] = wr.getRequestDate();
                row[2] = wr.getSender().getPerson().getFirstName();
                row[3] = ((LabWorkRequest)wr).getPatient().getFirstName();
                row[4] = wr.getStatus();
                model.addRow(row);
                        
            }
        }
    }

