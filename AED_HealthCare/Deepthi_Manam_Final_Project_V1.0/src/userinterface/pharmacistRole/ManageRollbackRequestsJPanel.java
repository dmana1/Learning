    private void rollbackJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rollbackJButtonActionPerformed
        // TODO add your handling code here:
        
        int selectedRow = rollbackJTable.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "please select the request to rollback the drug");
            return;
        }
        
        RollbackWorkRequest rwr = (RollbackWorkRequest)rollbackJTable.getValueAt(selectedRow, 0);
        
        Drug drug = rwr.getDrug();
        for(InventoryItem inventoryItem :((PharmacyEnterprise) enterprise).getInventory().getInventoryList() ){
            if((inventoryItem.getDrug().getDrugName()).equals(drug.getDrugName())){
                inventoryItem.setQuantity(0);
            }
        }
          JOptionPane.showMessageDialog(null, "Successfully rollbacked the drug");
          rwr.setStatus("Completed");
          populateTable();
    }//GEN-LAST:event_rollbackJButtonActionPerformed


