    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) rollbackJTable.getModel();
        model.setRowCount(0);
        rollbackJTable.getTableHeader().setFont(new Font("Tahoma", Font.PLAIN, 18));

        for (WorkRequest wr : organization.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof RollbackWorkRequest) {
                Object[] row = new Object[6];
                row[0] = ((RollbackWorkRequest) wr);
                row[1] = wr.getMessage();
                row[2] = wr.getSender().getPerson().getFirstName();
                row[3] = ((RollbackWorkRequest) wr).getSenderEnterprise();
                row[4] = ((RollbackWorkRequest) wr).getDrug();
                row[5] = wr.getStatus();
                model.addRow(row);

            }
        }
    }

