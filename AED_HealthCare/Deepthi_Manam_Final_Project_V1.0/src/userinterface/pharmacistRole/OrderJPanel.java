         String mailtoEmail = JOptionPane.showInputDialog("ENTER THE RECEPIENT'S MAIL ID");
        String subject = JOptionPane.showInputDialog("ENTER THE SUBJECT");
       String  message = JOptionPane.showInputDialog("ENTER THE MESSAGE");
