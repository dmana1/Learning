    private void populateFields(){
        Patient patient = prescriptionRequest.getPatient();
        nameJLabel.setText(patient.getFirstName());
        emailJLabel.setText(patient.getEmailId());
        phoneNumberJLabel.setText(String.valueOf(patient.getPhoneNumber()));
        hospitalJLabel.setText(prescriptionRequest.getSenderHospital());
        hospitalAddressJLabel.setText(prescriptionRequest.getSenderHospitalAddress());
    }
    
    private void populateTable(){
        DefaultTableModel model = (DefaultTableModel) orderJTable.getModel();
        model.setRowCount(0);
        orderJTable.getTableHeader().setFont(new java.awt.Font("Tahoma", java.awt.Font.PLAIN, 18));

        ArrayList<Medication> medicinesList = prescriptionRequest.getMedicinesList();
        if (medicinesList != null) {
            for (Medication medication : medicinesList) {
                Object[] row = new Object[3];
                row[0] = medication.getDrug().getDrugModelNo();
                row[1] = medication.getDrug().getDrugName();
                row[2] = medication.getDosage();
                model.addRow(row);
            }
        }
    }
