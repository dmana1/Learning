    private void reportToManufacturerJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reportToManufacturerJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = adverseEventJTable.getSelectedRow();
        if(selectedRow <0 ){
            JOptionPane.showMessageDialog(null, "Select the adverse event request");
            return;
        }
        
        AdverseEventRequest aer = (AdverseEventRequest)adverseEventJTable.getValueAt(selectedRow, 0);
        if(aer.getStatus().equals("Completed")){
             JOptionPane.showMessageDialog(null, "Request is already completed.");
            return;
        }
        DrugCompanyEnterprise drugCompanyEnterprise = aer.getDrugCompanyEnterprise();
        for(Organization organization : drugCompanyEnterprise.getOrganizationDirectory().getOrganizationList()){
            if(organization instanceof DrugManagement){
                DrugAdverseEventRequest daer = new DrugAdverseEventRequest();
                daer.setAdverseEvent(aer.getAdverseEvent());
                daer.setSender(userAccount);
                daer.setDrug(aer.getDrug());
                daer.setStatus("Pending");
                daer.setRequestId(new Random().nextInt(100));
                daer.setSenderHospital(enterprise.getName());
                organization.getWorkQueue().getWorkRequestList().add(daer);
            }
        }
        
        aer.setStatus("Completed");
        aer.setResult("Reported to Drug Company");
        
        populateTable();
        
    }//GEN-LAST:event_reportToManufacturerJButtonActionPerformed

