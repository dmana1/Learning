    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) adverseEventJTable.getModel();
        model.setRowCount(0);
        adverseEventJTable.getTableHeader().setFont(new Font("Tahoma", Font.PLAIN, 18));

        for (WorkRequest wr : organization.getWorkQueue().getWorkRequestList()) {
            if (wr instanceof AdverseEventRequest) {
                Object[] row = new Object[7];
                row[0] = wr;
                row[1] = wr.getSender().getPerson().getFirstName();
                row[2] = ((AdverseEventRequest) wr).getDrugCompanyEnterprise();
                row[3] = ((AdverseEventRequest) wr).getDrug();
                row[4] = ((AdverseEventRequest) wr).getAdverseEvent();
                row[5] = wr.getStatus();
                row[6] = ((AdverseEventRequest) wr).getResult();
                model.addRow(row);

            }
        }
    }

