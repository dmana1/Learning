    private void viewPatientHistoryJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewPatientHistoryJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = emergencyReqJTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Select the request");
            return;
        }

        EmergencyRequest er = (EmergencyRequest) emergencyReqJTable.getValueAt(selectedRow, 0);

        //check whether  request is  completed
        if (!(er.getStatus().equals("Processed By Admin"))) {
            JOptionPane.showMessageDialog(null, "Request is not processed yet.");
            return;
        }

        ViewEmergencyPatientHistoryJPanel panel = new ViewEmergencyPatientHistoryJPanel(system, userProcessContainer, enterprise, organization, userAccount,
                er.getPatientOfHospital(), er.getEncounterList(), er.getAllergyList(), er.getAdverseList(), er.getLabTestList());
        userProcessContainer.add("ViewEmergencyPatientHistoryJPanel", panel);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        
        
    }//GEN-LAST:event_viewPatientHistoryJButtonActionPerformed

