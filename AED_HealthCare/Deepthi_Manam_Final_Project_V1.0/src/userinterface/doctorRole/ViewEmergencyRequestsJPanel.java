   private void populateTable(){
        DefaultTableModel model = (DefaultTableModel)emergencyReqJTable.getModel();
        model.setRowCount(0);
         emergencyReqJTable.getTableHeader().setFont(new Font( "Tahoma" , Font.PLAIN, 18 ));
        for(WorkRequest wr : userAccount.getWorkQueue().getWorkRequestList()){
            if(wr instanceof EmergencyRequest){
                Object[] row = new Object[5];
                row[0] = ((EmergencyRequest)wr);
                row[1] = ((EmergencyRequest)wr).getRequestDate();
                row[2] = wr.getSender().getPerson().getFirstName();
                row[3] = ((EmergencyRequest)wr).getLastName();
                row[4] = wr.getStatus();
                model.addRow(row);
            }
        }
    }
