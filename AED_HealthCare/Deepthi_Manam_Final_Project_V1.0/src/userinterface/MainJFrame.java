    private void loginJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginJButtonActionPerformed
        // TODO add your handling code here:
        // Get user name
        String userName = userNameJTextField.getText();
        // Get Password
        char[] passwordCharArray = passwordJPasswordField.getPassword();
        String password = String.valueOf(passwordCharArray);

        UserAccount userAccount = null;
        //first check in useraccount directory of Sytem.If not found in system then  useraccount directory of Enterprise, then  useraccount directory of organization

        Enterprise inEnterprise = null;
        Organization Inorganization = null;
        //first check in useraccount directory of Sytem.
        userAccount = system.getUserAccountDirectory().authenticateUser(userName, password);
        if (userAccount == null) {
            //As it is not found in the userdirectory of sytem, check in the userDIrectory of Enterprise
            for (Network network : system.getNetworkList()) {
                for (Enterprise enterprise : network.getEnterpriseDir().getEnterpriseList()) {
                    userAccount = enterprise.getUserAccountDirectory().authenticateUser(userName, password);

                    //As it is not found in the userdirectory of the Enterpise, check in the userdirectory of the Organizations present in the enterprise
                    if (userAccount == null) {
                        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {

                            userAccount = organization.getUserAccountDirectory().authenticateUser(userName, password);
                            if (userAccount != null) {
                                inEnterprise = enterprise;
                                Inorganization = organization;
                                break;
                            }
                        }
                    } else {
                        //IF it is present in the userAccount of that enterprise.
                        inEnterprise = enterprise;
                        break;
                    }
                    if (Inorganization != null) {
                        break;
                    }

                }
                if (inEnterprise != null) {
                    break;
                }
            }
        }
        //check userAccount exists or not
        if (userAccount == null) {
            JOptionPane.showMessageDialog(null, "UserName and Password does not exists");
            return;
        } else {
             Logger.getLogger(MainJFrame.class.getName()).log(Level.ALL, userAccount.getPerson().getFirstName() + " has logged into the application");

            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("WorkArea", userAccount.getRole().createWorkArea(userProcessContainer, system, inEnterprise, Inorganization, userAccount));
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_loginJButtonActionPerformed

