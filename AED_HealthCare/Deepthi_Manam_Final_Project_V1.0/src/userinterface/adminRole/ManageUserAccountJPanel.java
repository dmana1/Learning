    private void submitJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitJButtonActionPerformed
        // TODO add your handling code here:
        String userName = userNameJTextField.getText();
        String password = passwordJTextField.getText();
        Organization organization = (Organization) organizationJComboBox.getSelectedItem();
        String  name =  (String)employeeJComboBox.getSelectedItem();
        String[] names = name.split(" ");
        Person person = organization.getPersonDirectory().searchByFirstAndLastName(names[0], names[1]);
        Role role = (Role) roleJComboBox.getSelectedItem();
        
        organization.getUserAccountDirectory().createUserAccount(userName, password, person, role);
        JOptionPane.showMessageDialog(null, "Successfully created User Account");
        populateTable();
    }//GEN-LAST:event_submitJButtonActionPerformed

