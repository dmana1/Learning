    public void populateOrganizationComboBox() {
        organizationJComboBox.removeAllItems();

        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            organizationJComboBox.addItem(organization);
        }
    }

    public void populateEmployeeComboBox(Organization organization) {
        employeeJComboBox.removeAllItems();

        for (Person person : organization.getPersonDirectory().getPersonsList()) {
            employeeJComboBox.addItem(person.getFirstName() + " " + person.getLastName());
        }
    }

    private void populateRoleComboBox(Organization organization) {
        roleJComboBox.removeAllItems();
        for (Role role : organization.getSupportedRole()) {
            roleJComboBox.addItem(role);
        }
    }

    public void populateTable() {

        DefaultTableModel model = (DefaultTableModel) userAccountsJTable.getModel();
        model.setRowCount(0);
        userAccountsJTable.getTableHeader().setFont(new Font("Tahoma", Font.PLAIN, 18));

        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount ua : organization.getUserAccountDirectory().getUserAccountList()) {
                Object row[] = new Object[5];
                row[0] = ua;
                row[1] = ua.getPassword();
                row[2] = ua.getPerson().getFirstName() + " " + ua.getPerson().getLastName();
                row[3] = ua.getRole();
                row[4] = organization;
                ((DefaultTableModel) userAccountsJTable.getModel()).addRow(row);
            }
        }
    }

