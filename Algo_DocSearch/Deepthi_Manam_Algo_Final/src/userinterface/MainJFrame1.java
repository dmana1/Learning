   private void populateSearchResults(Map<String,Double> vsRankedMap) {
       DefaultTableModel model = (DefaultTableModel) searchResultsJTable.getModel();
       model.setRowCount(0);
       searchResultsJTable.getTableHeader().setFont(new Font("Tahoma", Font.PLAIN, 18));
       RankComparator comp = new RankComparator((TreeMap) vsRankedMap);
       TreeMap<String, Double> finalRankedMap = new TreeMap<String, Double>(comp);
       finalRankedMap.putAll(vsRankedMap);

       Iterator it = finalRankedMap.entrySet().iterator();
       while(it.hasNext()) {
           Map.Entry pair = (Map.Entry) it.next();
           Object[] row = new Object[2];
           row[0] = pair.getKey();
           row[1] = pair.getValue();
           model.addRow(row);

       }
        
     }
    
